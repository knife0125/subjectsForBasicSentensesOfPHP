<?php
	$str = "hogehoge";
	// $paramという変数に文字列を入れて変数を作って下さい
    $param = "fugafuga";

	// $param と $str を比較して、
	// 同じ文字列であれば"同じです"と表示し、違う文字列であれば"違います"と表示する
	// if 文の制御を書いてみてください！
    if ($str === $param) {
        echo "同じですね";
        echo "\n";
    } else {
        echo "違いますね";
        echo "\n";
    }
